package clashsoft.cslib.math;


public class CSMath
{
	public static int abs(int d)
	{
		return Math.abs(d);
	}
	
	public static long abs(long d)
	{
		return Math.abs(d);
	}
	
	public static float abs(float d)
	{
		return Math.abs(d);
	}
	
	public static double abs(double d)
	{
		return Math.abs(d);
	}
	
	public static int floor_float_int(float f)
	{
		return (int) f;
	}
	
	public static long floor_float_long(float f)
	{
		return (long) f;
	}
	
	public static float floor_float(float f)
	{
		return (float) ((int) f);
	}
	
	public static double floor_float_double(float f)
	{
		return (double) ((int) f);
	}
	
	public static int floor_double_int(double f)
	{
		return (int) f;
	}
	
	public static long floor_double_long(double f)
	{
		return (long) f;
	}
	
	public static float floor_double_float(double f)
	{
		return (float) ((int) f);
	}
	
	public static double floor_double(double f)
	{
		return (double) ((int) f);
	}
}
